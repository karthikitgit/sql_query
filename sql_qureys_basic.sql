Joining Tables:
---------------------------------------------------------------------------------------------------------------------------
CREATE TABLE Grades (
    student_id INT,
    subject VARCHAR(50),
    score INT
);

INSERT INTO Grades (student_id, subject, score)
VALUES (1, 'Math', 90),
       (2, 'Math', 85),
       (1, 'Science', 80);

SELECT Students.name, Grades.subject, Grades.score
FROM Students
INNER JOIN Grades ON Students.id = Grades.student_id;